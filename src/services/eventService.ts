/**
 * Convert Firebase EventData to frontend Event type
 * This adapter ensures compatibility between Firebase schema and UI components
 */
import type { Event } from '@/features/events/event.types';

export const convertEventDataToEvent = (eventData: EventData): Event => {
  // Calculate availability
  const totalSold = eventData.tickets?.reduce((sum, ticket) => sum + (ticket.sold || 0), 0) || 0;
  const totalCapacity = eventData.venue.capacity;
  
  // Calculate ticket price range
  const prices = eventData.tickets?.map(t => t.price) || [0];
  const minPrice = Math.min(...prices);
  
  return {
    id: eventData.eventId || '',
    title: eventData.basicInfo.title,
    description: eventData.basicInfo.description,
    
    // Date & Time
    date: new Date(`${eventData.schedule.startDate}T${eventData.schedule.startTime}`),
    time: eventData.schedule.startTime,
    endDate: new Date(`${eventData.schedule.endDate}T${eventData.schedule.endTime}`),
    endTime: eventData.schedule.endTime,
    
    // Location
    location: {
      venue: eventData.venue.name,
      address: eventData.venue.address,
      city: eventData.venue.city,
      coordinates: eventData.venue.coordinates,
    },
    
    // Pricing
    price: minPrice,
    currency: 'INR',
    
    // Capacity
    capacity: totalCapacity,
    attendees: totalSold,
    available: totalCapacity - totalSold,
    
    // Categorization
    category: eventData.basicInfo.category,
    tags: [],
    
    // Media
    image: eventData.basicInfo.bannerImage || undefined,
    video: eventData.basicInfo.videoUrl || undefined,
    
    // Organizer
    organizer: {
      id: eventData.organizerId,
      name: eventData.organizerEmail || 'Event Organizer',
      email: eventData.organizerEmail,
    },
    
    // Status
    status: eventData.status,
    isFeatured: false,
    isTrending: (eventData.analytics?.views || 0) > 100,
    
    // Ratings (placeholder - can be enhanced later)
    rating: 4.5,
    reviews: 0,
    
    // Additional
    ticketTiers: eventData.tickets?.map(ticket => ({
      id: ticket.id || Math.random().toString(36).substr(2, 9),
      name: ticket.name,
      price: ticket.price,
      quantity: ticket.quantity,
      available: ticket.quantity - (ticket.sold || 0),
      sold: ticket.sold || 0,
    })) || [],
    
    // Timestamps
    createdAt: eventData.createdAt || new Date(),
    updatedAt: eventData.updatedAt || new Date(),
  };
};

/**
 * Get published events for public catalog
 * Filters only published events and converts to Event type
 */
export const getPublishedEvents = async (): Promise<Event[]> => {
  try {
    const eventsRef = ref(rtdb, 'events');
    
    // First try to get all events to debug
    const allEventsSnapshot = await get(eventsRef);
    console.log('📊 All events in DB:', allEventsSnapshot.exists() ? Object.keys(allEventsSnapshot.val()).length : 0);
    
    if (allEventsSnapshot.exists()) {
      const allEventsObj = allEventsSnapshot.val();
      console.log('📊 Event statuses:', Object.values(allEventsObj).map((e: any) => e.status));
    }
    
    const eventsQuery = query(
      eventsRef,
      orderByChild('status'),
      equalTo('published')
    );
    
    const snapshot = await get(eventsQuery);
    console.log('📊 Published events found:', snapshot.exists());
    
    if (snapshot.exists()) {
      const eventsObj = snapshot.val();
      const eventDataArray = Object.values(eventsObj) as EventData[];
      console.log('📊 Published events count:', eventDataArray.length);
      
      // Convert to Event type - temporarily disable date filter for debugging
      const now = new Date();
      const convertedEvents = eventDataArray.map(convertEventDataToEvent);
      console.log('📊 Converted events:', convertedEvents.length);
      console.log('📊 Event dates:', convertedEvents.map(e => e.date));
      
      // Filter future events but be more lenient
      const futureEvents = convertedEvents.filter(event => {
        const eventDate = new Date(event.date);
        const isPast = eventDate < now;
        console.log(`📊 Event "${event.title}" date: ${eventDate}, isPast: ${isPast}`);
        return !isPast || isNaN(eventDate.getTime()); // Keep if future OR invalid date
      });
      
      console.log('📊 Future events after filter:', futureEvents.length);
      
      // If no future events, return all converted events for now
      if (futureEvents.length === 0 && convertedEvents.length > 0) {
        console.log('📊 Returning all events (no future events found)');
        return convertedEvents.sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
      }
      
      return futureEvents.sort((a, b) => new Date(a.date).getTime() - new Date(b.date).getTime());
    }
    
    console.log('📊 No published events found in database');
    return [];
  } catch (error) {
    console.error('❌ Error fetching published events:', error);
    throw error;
  }
};

/**
 * Get published events with real-time updates
 * Returns a function to unsubscribe
 */
export const subscribeToPublishedEvents = (
  callback: (events: Event[]) => void
): (() => void) => {
  const eventsRef = ref(rtdb, 'events');
  const eventsQuery = query(
    eventsRef,
    orderByChild('status'),
    equalTo('published')
  );
  
  const unsubscribe = onValue(eventsQuery, (snapshot) => {
    if (snapshot.exists()) {
      const eventsObj = snapshot.val();
      const eventDataArray = Object.values(eventsObj) as EventData[];
      
      const now = new Date();
      const events = eventDataArray
        .map(convertEventDataToEvent)
        .filter(event => new Date(event.date) >= now)
        .sort((a, b) => new Date(a.date).getTime() - new Date(b.date).getTime());
      
      callback(events);
    } else {
      callback([]);
    }
  }, (error) => {
    console.error('❌ Error in events subscription:', error);
  });
  
  return () => off(eventsRef, 'value', unsubscribe);
};

