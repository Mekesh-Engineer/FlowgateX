/**
 * Creates a new event in Firebase Realtime Database
 * @param eventData - The form data from the event creation page
 * @param userId - The organizer's user ID
 * @returns The unique event ID (key) generated by Firebase
 * @deprecated Use createEvent instead
 */
export const createEventInRealtimeDB = async (eventData: any, userId: string) => {
  try {
    // 1. Create a reference to the 'events' path
    const eventsRef = ref(rtdb, 'events');

    // 2. Generate a unique key for the new event
    const newEventRef = push(eventsRef);

    // 3. Construct the payload with the structure defined in the JSON schema
    const payload = {
      eventId: newEventRef.key,
      organizerId: userId,
      organizerEmail: eventData.organizerEmail || null,
      status: 'published',
      createdAt: serverTimestamp(), // Server-side timestamp
      
      // Basic Information
      basicInfo: {
        title: eventData.title,
        description: eventData.description,
        category: eventData.category,
        videoUrl: eventData.videoUrl || null,
        bannerImage: eventData.image || null
      },
      
      // Schedule Details
      schedule: {
        startDate: eventData.startDate,
        startTime: eventData.startTime,
        endDate: eventData.endDate,
        endTime: eventData.endTime,
        timezone: eventData.timezone,
        recurring: eventData.recurring,
        frequency: eventData.recurring ? eventData.recurringFrequency : null
      },
      
      // Venue Information
      venue: {
        name: eventData.venueName,
        address: eventData.address,
        city: eventData.city,
        capacity: Number(eventData.capacity) || 0,
        coordinates: eventData.coordinates || { lat: 0, lng: 0 },
        zones: eventData.zones.map((z: any) => ({
          name: z.name,
          capacity: Number(z.capacity) || 0
        }))
      },
      
      // Ticket Configuration
      tickets: eventData.tickets.map((t: any) => ({
        name: t.name,
        price: Number(t.price) || 0,
        quantity: Number(t.quantity) || 0,
        earlyBird: t.earlyBird || false
      })),
      
      // Promo Codes
      promoCodes: eventData.promoCodes.map((p: any) => ({
        code: p.code,
        discount: p.discount,
        limit: Number(p.limit) || 0
      })),
      
      // IoT Gates (only store assigned gates)
      iotGates: eventData.gates
        .filter((g: any) => g.assigned)
        .map((g: any) => ({
          id: g.id,
          name: g.name,
          type: g.type,
          assigned: g.assigned,
          accessRule: g.accessRule,
          sensorConfig: g.sensorConfig
        })),
      
      // Advanced Settings
      settings: {
        amenities: eventData.amenities,
        services: eventData.services,
        ageRestriction: eventData.ageRestriction,
        refundPolicy: eventData.refundPolicy || ''
      }
    };

    // 4. Save the data to the specific reference
    await set(newEventRef, payload);
    
    console.log("✅ Event created successfully in Realtime Database!");
    console.log("📍 Event ID:", newEventRef.key);
    return newEventRef.key;

  } catch (error) {
    console.error("❌ Error creating event:", error);
    throw error;
  }
};

