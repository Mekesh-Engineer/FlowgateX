/**
 * Update ticket quantities after booking
 * This should be called after successful payment
 */
export const updateTicketQuantities = async (
  eventId: string,
  ticketUpdates: Record<string, number> // tierId: quantityBooked
): Promise<void> => {
  try {
    const eventRef = ref(rtdb, `events/${eventId}`);
    const snapshot = await get(eventRef);
    
    if (!snapshot.exists()) {
      throw new Error('Event not found');
    }
    
    const eventData = snapshot.val() as EventData;
    
    // Update ticket sold counts
    const updatedTickets = eventData.tickets.map(ticket => {
      const ticketId = ticket.id || '';
      const quantityBooked = ticketUpdates[ticketId] || 0;
      
      return {
        ...ticket,
        sold: (ticket.sold || 0) + quantityBooked,
      };
    });
    
    await update(eventRef, {
      tickets: updatedTickets,
      updatedAt: serverTimestamp(),
    });
    
    console.log('✅ Ticket quantities updated for event:', eventId);
  } catch (error) {
    console.error('❌ Error updating ticket quantities:', error);
    throw error;
  }
};

/**
 * Get all bookings for a user
 */
export const getBookingsByUser = async (userId: string): Promise<BookingData[]> => {
  try {
    const bookingsRef = ref(rtdb, 'bookings');
    const userBookingsQuery = query(bookingsRef, orderByChild('userId'), equalTo(userId));
    const snapshot = await get(userBookingsQuery);
    
    if (!snapshot.exists()) {
      return [];
    }
    
    const bookings: BookingData[] = [];
    snapshot.forEach((childSnapshot) => {
      bookings.push({
        id: childSnapshot.key!,
        ...childSnapshot.val(),
      });
    });
    
    return bookings;
  } catch (error) {
    console.error('❌ Error fetching user bookings:', error);
    return [];
  }
};

